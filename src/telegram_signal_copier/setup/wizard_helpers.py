        f"TELEGRAM_API_HASH={fields.get('telegram_api_hash', '')}",
        f"TELEGRAM_PHONE_NUMBER={fields.get('telegram_phone', '')}",
        "TELEGRAM_SESSION_NAME=telegram-signal-copier",
        "",
        "# ── Telegram Sources (group names; numeric IDs added after first discovery) ─",
        f"TELEGRAM_SOURCES={fields.get('telegram_sources', '')}",
        "",
        "# ── MT5 Account ─────────────────────────────────────────────────────────────",
        f"MT5_LOGIN={fields.get('mt5_login', '')}",
        f"MT5_PASSWORD={fields.get('mt5_password', '')}",
        f"MT5_SERVER={fields.get('mt5_server', '')}",
        f"MT5_SYMBOL_SUFFIX={fields.get('mt5_symbol_suffix', '')}",
        "",
        "# ── AI Provider (SambaNova free tier — swap key/URL for any OpenAI-compatible API)",
        f"OPENAI_API_KEY={fields.get('openai_api_key', '')}",
        "OPENAI_MODEL=Llama-4-Maverick-17B-128E-Instruct",
        "OPENAI_BASE_URL=https://api.sambanova.ai/v1",
        "",
