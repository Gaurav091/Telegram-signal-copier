        "ALLOWED_SYMBOLS=XAUUSD,EURUSD,GBPUSD,USDJPY,US30,NAS100,BTCUSD,ETHUSD,XAGUSD,USOIL,SPX500",
        "DRY_RUN=false",
        "SIGNAL_MAX_AGE_SECONDS=5400",
